	<view class="movie-score-wapper">
		<image 
		v-for="(yellow, index) in yellowScore"
		src="../static/icons/Star-yellow.png" 
		class="star-ico"
		:key="index"></image>
		<image 
		v-for="(gray, index) in grayScore"
		src="../static/icons/Star-huise.png" 
		class="star-ico"
		:key="index"></image>
		<view class="movie-score" v-if="showNum == 1">
			{{innerScore}}
		</view>
